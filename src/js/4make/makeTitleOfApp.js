function makeTitleOfApp()
{
    let theLink = ce('a');
    theLink.href = 'https://github.com/ChristopherAndrewTopalian/CATopalian_JavaScript_File_Navigator';
    theLink.target = '_blank';
    theLink.textContent = 'CATopalian JavaScript File Navigator';
    theLink.style.fontFamily = 'Arial';
    theLink.style.fontSize = '14px';
    theLink.style.color = 'rgb(130, 130, 130)';
    theLink.style.fontWeight = 'bold';
    theLink.style.textDecoration = 'none';
    theLink.style.zIndex = 1;
    return theLink;
}

